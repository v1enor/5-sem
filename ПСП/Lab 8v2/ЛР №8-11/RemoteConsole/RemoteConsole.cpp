string GetErrorMsgText(int code)
{
	string msgText;
	switch (code)
	{
	default: msgText = "***ERROR***";
		break;
	};
	return msgText;
}
string SetPipeError(string msgText, int code)
{
	return msgText + GetErrorMsgText(code);
};

int _tmain(int argc, _TCHAR* argv[])
{
	setlocale(LC_ALL, "Rus");
	SetConsoleTitle("Remote Console"); // заголовок консольного окна

	try
	{
		printf("\nCommands:\n 1 - Start  \t \n 2 - Stop  \t \n 3 - Exit  \t \n 4 - Statistics  \n 5 - Wait  \t\n 6 - Shutdown\n\n");
		char ReadBuf[50] = "";// Буфер для примема сообшения от сервера
		char WriteBuf[2] = "";// Буфер для отправки сообщения серверу
		DWORD nBytesRead;
		DWORD nBytesWrite;

		int Code = 0;// Код команды

		char serverName[256];
		char PipeName[512];


		cout << "Enter server name: ";
		cin >> serverName;
		sprintf(PipeName, "\\\\%s\\pipe\\smc", serverName); // Имя канала для подключения к серверу (имя сервера)


		BOOL fSuccess;

		SECURITY_ATTRIBUTES SecurityAttributes; // Атрибуты безопасности
		SECURITY_DESCRIPTOR SecurityDescriptor; // Описание безопасности

		fSuccess = InitializeSecurityDescriptor( // Инициализация описания безопасности
			&SecurityDescriptor,
			SECURITY_DESCRIPTOR_REVISION);

		if (!fSuccess) { // Если инициализация описания безопасности не прошла успешно
			throw new string("InitializeSecurityDescriptor(): Ошибка");
		}

		fSuccess = SetSecurityDescriptorDacl( // Установка описания безопасности
			&SecurityDescriptor,
			TRUE,
			NULL,
			FALSE);

		if (!fSuccess) { // Если установка описания безопасности не прошла успешно
			throw new string("SetSecurityDescriptorDacl(): Ошибка");
		}

		SecurityAttributes.nLength = sizeof(SecurityAttributes); // Размер структуры
		SecurityAttributes.lpSecurityDescriptor = &SecurityDescriptor; // Указатель на описание безопасности
		SecurityAttributes.bInheritHandle = FALSE; // Наследование дескриптора

		HANDLE hNamedPipe = CreateFile(PipeName, GENERIC_READ | GENERIC_WRITE, 0, NULL, OPEN_EXISTING, 0, &SecurityAttributes); // Создание дескриптора канала

		do 
		{
			printf("Command: "); // Вывод приглашения ввода команды
			scanf("%d", &Code); // Ввод команды
			if (Code > 0 && Code < 7)
			{
				sprintf(WriteBuf, "%d", Code - 1);
				if (!WriteFile(hNamedPipe, WriteBuf, strlen(WriteBuf) + 1, &nBytesWrite, NULL))
					throw "WriteFile: Ошибка ";
				if (ReadFile(hNamedPipe, ReadBuf, sizeof(ReadBuf), &nBytesRead, NULL))
					cout << ReadBuf << endl;
				else
					throw "ReadFile: Ошибка ";

			}
			else if (Code > 9) printf("Неверная команда.\n\n");
		} while (Code != 3 && Code != 6);
	}
	catch (char* ErrorPipeText)
	{
		printf("%s", ErrorPipeText);
		cout << GetLastError() << endl;
	}
	system("pause");
	return 0;
}
